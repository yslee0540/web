package 연산자;

public class 비교연산자2 {

	public static void main(String[] args) {
		// 비교연산자(== != > >=)
		//기본형 데이터만 가능
		int id = 1111;
		int id2 = 2222;
		
		int pw = 3333;
		int pw2 = 3333;
		
		System.out.println("아이디 비교 " + (id == id2));
		System.out.println("패스워드 비교 " + (pw == pw2));
		
	}

}
